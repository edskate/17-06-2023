x = input('Digite algo')
print('È um numero? ', x.isnumeric())
print('È um alfanumerico (numeros e letras)? ', x.isalnum())
print('È um alfabetico (letras)? ' , x.isalpha())
print('Esta em minusculo? ' , x.islower())
print('Esta em maiusculo? ' , x.isupper())
print('È somente espaço? ' , x.isspace())
print('Esta capitalizada (possui maiusculas e minusculas)' , x.istitle())

